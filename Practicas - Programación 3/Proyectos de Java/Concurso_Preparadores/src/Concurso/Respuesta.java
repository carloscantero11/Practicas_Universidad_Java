package Concurso;

public class Respuesta {
    
    private String id;
    private String resp;
    
    public Respuesta (String i, String r) {
        id = i;
        resp = r;    
    }

    public String getId() {
        return id;
    }

    public String getResp() {
        return resp;
    }
    
    
    
    
    
    
    
    
    
    
}
