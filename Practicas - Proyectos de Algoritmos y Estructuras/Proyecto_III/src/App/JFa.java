    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = Tabla.getSelectedRow();
        if(fila>=0){
            modelo.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccionar fila");
        }
        
        
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        int fila = Tabla.getRowCount();
        for (int i=fila-1; i>=0; i--){
            modelo.removeRow(i);
        }
    }//GEN-LAST:event_btnLimpiarActionPerformed

    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        int seleccion = Tabla.getSelectedRow();
        
        txtNombre.setText(Tabla.getValueAt(seleccion,0).toString());
        txtApellido.setText(Tabla.getValueAt(seleccion,1).toString());
        txtCedula.setText(Tabla.getValueAt(seleccion,2).toString());
        txtCorreo.setText(Tabla.getValueAt(seleccion,3).toString());
        
        filas = seleccion;
    }//GEN-LAST:event_TablaMouseClicked

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String []info =new String[4];
        info[0] = txtNombre.getText();
        info[1] = txtApellido.getText();
        info[2] = txtCedula.getText();
        info[3] = txtCorreo.getText();
        
        for(int i=0; i<Tabla.getColumnCount();i++){
            modelo.setValueAt(info[i],filas,i);
            
        }
    }//GEN-LAST:event_btnModificarActionPerformed

